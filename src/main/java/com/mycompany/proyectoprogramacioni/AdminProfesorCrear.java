    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Profesor p = new Profesor();
        p.nombre = jTextField1.getText();
        p.apellido = jTextField2.getText();
        p.usuario = jTextField3.getText();
        if (jTextField4.getText().matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]+$")) {
            p.password = jTextField4.getText();
        } 
        else {
            JOptionPane.showMessageDialog(null, "La contraseña debe contener al menos una mayúscula, minúscula, número y carácter especial.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        ProyectoProgramacionI.profesores.add(p);
        JOptionPane.showMessageDialog(null, "Datos guardados");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

