    private void Guardar() {
        String contracenaTxt = jTextField4.getText();
        
        Alumno a = new Alumno();
        a.setCarne(jTextField1.getText());
        a.setNombre(jTextField2.getText());
        a.setApellido(jTextField3.getText());
        a.setContracena(jTextField4.getText());
        a.setGenero(jTextField5.getText());
        
        if (a.carne.length() == 8 && a.carne.matches("[a-zA-Z0-9]+")) {
        } else {
            JOptionPane.showMessageDialog(null, "El carné debe tener como maximo 8 caracteres y debe ser alfanumerico", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (contracenaTxt.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]+$")) {
        } else {
            JOptionPane.showMessageDialog(null, "La contraseña debe contener al menos una mayúscula, minúscula, número y carácter especial.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ProyectoProgramacionI.alumnos.add(a);
        JOptionPane.showMessageDialog(null, "Datos guardados");
        Limpiar();
    }

